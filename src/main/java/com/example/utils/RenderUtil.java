    public static void renderWayPoint(List<String> lines, double x, double y, double z, int color, float partialTicks) {
        GlStateManager.alphaFunc(516, 0.1F);

        GlStateManager.pushMatrix();

        Entity viewer = Minecraft.getMinecraft().getRenderViewEntity();
        double viewerX = viewer.lastTickPosX + (viewer.posX - viewer.lastTickPosX) * partialTicks;
        double viewerY = viewer.lastTickPosY + (viewer.posY - viewer.lastTickPosY) * partialTicks;
        double viewerZ = viewer.lastTickPosZ + (viewer.posZ - viewer.lastTickPosZ) * partialTicks;

        double _x = x - viewerX + 0.5f;
        double _y = y - viewerY - viewer.getEyeHeight();
        double _z = z - viewerZ + 0.5f;

        double distSq = _x * _x + _y * _y + _z * _z;
        double dist = Math.sqrt(distSq);
        if (distSq > 144) {
            _x *= 12 / dist;
            _y *= 12 / dist;
            _z *= 12 / dist;
        }
        GlStateManager.translate(_x, _y, _z);
        GlStateManager.translate(0, viewer.getEyeHeight(), 0);

        lines = new ArrayList<>(lines);
        lines.add(EnumChatFormatting.YELLOW.toString() + Math.round(dist) + "m");
        renderNametag(lines, color);

        GlStateManager.popMatrix();

        GlStateManager.disableLighting();
    }

    private static void renderNametag(List<String> lines, int color) {
        FontRenderer fontrenderer = Minecraft.getMinecraft().fontRendererObj;
        float f = 1.6F;
        float f1 = 0.016666668F * f;
        GlStateManager.pushMatrix();
        GL11.glNormal3f(0.0F, 1.0F, 0.0F);
        GlStateManager.rotate(-Minecraft.getMinecraft().getRenderManager().playerViewY, 0.0F, 1.0F, 0.0F);
        GlStateManager.rotate(Minecraft.getMinecraft().getRenderManager().playerViewX, 1.0F, 0.0F, 0.0F);
        GlStateManager.scale(-f1, -f1, f1);
        GlStateManager.disableLighting();
        GlStateManager.depthMask(false);
        GlStateManager.disableDepth();
        GlStateManager.enableBlend();
        GlStateManager.tryBlendFuncSeparate(770, 771, 1, 0);
        Tessellator tessellator = Tessellator.getInstance();
        WorldRenderer worldrenderer = tessellator.getWorldRenderer();
        int i = 0;

        for (String str : lines) {
            int j = fontrenderer.getStringWidth(str) / 2;

            GlStateManager.disableTexture2D();
            worldrenderer.begin(7, DefaultVertexFormats.POSITION_COLOR);
            float red = (float)(color >> 16 & 0xFF) / 255.0f;
            float green = (float)(color >> 8 & 0xFF) / 255.0f;
            float blue = (float)(color & 0xFF) / 255.0f;
            worldrenderer.pos(-j - 1, -1 + i, 0.0D).color(red, green, blue, 0.25F).endVertex();
            worldrenderer.pos(-j - 1, 8 + i, 0.0D).color(red, green, blue, 0.25F).endVertex();
            worldrenderer.pos(j + 1, 8 + i, 0.0D).color(red, green, blue, 0.25F).endVertex();
            worldrenderer.pos(j + 1, -1 + i, 0.0D).color(red, green, blue, 0.25F).endVertex();
            tessellator.draw();
            GlStateManager.enableTexture2D();
            fontrenderer.drawString(str, -fontrenderer.getStringWidth(str) / 2, i, 553648127);
            GlStateManager.depthMask(true);

            fontrenderer.drawString(str, -fontrenderer.getStringWidth(str) / 2, i, -1);
            GlStateManager.translate(0, 10f, 0);
        }
        GlStateManager.enableDepth();
        GlStateManager.enableBlend();
        GlStateManager.color(1.0F, 1.0F, 1.0F, 1.0F);
        GlStateManager.popMatrix();
    }
