    // expandBySmallAmt so that the box doesnt look glitchy if showThroughWalls is enabled
    public static void renderBoundingBox(double _x, double _y, double _z, int rgb, float alphaMult, float partialTicks, boolean showThroughWalls, boolean expandBySmallAmt) {
        Entity viewer = Minecraft.getMinecraft().getRenderViewEntity();
        double viewerX = viewer.lastTickPosX + (viewer.posX - viewer.lastTickPosX) * partialTicks;
        double viewerY = viewer.lastTickPosY + (viewer.posY - viewer.lastTickPosY) * partialTicks;
        double viewerZ = viewer.lastTickPosZ + (viewer.posZ - viewer.lastTickPosZ) * partialTicks;
        double x = _x - viewerX;
        double y = _y - viewerY;
        double z = _z - viewerZ;

        AxisAlignedBB bb = new AxisAlignedBB(x, y, z, x + 1, y + 1, z + 1);
        if (expandBySmallAmt) bb = bb.expand(0.01, 0.01, 0.01);

        if (showThroughWalls) GlStateManager.disableDepth();
        GlStateManager.disableCull();
        GlStateManager.disableTexture2D();
        drawFilledBoundingBox(bb, alphaMult, rgb);
        GlStateManager.enableTexture2D();
        GlStateManager.enableCull();
        if (showThroughWalls) GlStateManager.enableDepth();
    }

